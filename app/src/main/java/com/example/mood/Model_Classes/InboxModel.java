package com.example.mood.Model_Classes;

public class InboxModel {

    String type;
    String received_msg;
    String sent_msg;
    String Time_receivd;
    String Time_sent;
    String contact;
    String name_sender;




    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getReceived_msg() {
        return received_msg;
    }

    public void setReceived_msg(String received_msg) {
        this.received_msg = received_msg;
    }

    public String getSent_msg() {
        return sent_msg;
    }

    public void setSent_msg(String sent_msg) {
        this.sent_msg = sent_msg;
    }

    public String getTime_receivd() {
        return Time_receivd;
    }

    public void setTime_receivd(String time_receivd) {
        Time_receivd = time_receivd;
    }

    public String getTime_sent() {
        return Time_sent;
    }

    public void setTime_sent(String time_sent) {
        Time_sent = time_sent;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getName_sender() {
        return name_sender;
    }

    public void setName_sender(String name_sender) {
        this.name_sender = name_sender;
    }
}
